  id         UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  token      TEXT UNIQUE NOT NULL, -- e.g. 'atta', 'tata' (stored in lowercase)
  hindi      TEXT NOT NULL,        -- e.g. 'आटा', 'टाटा'
  category   TEXT NOT NULL DEFAULT 'general' CHECK (category IN ('brand', 'product', 'qualifier', 'general')),
  created_at TIMESTAMPTZ NOT NULL DEFAULT now()
);

ALTER TABLE receipt_translations ENABLE ROW LEVEL SECURITY;

CREATE POLICY "Authenticated users can select receipt translations"
  ON receipt_translations FOR SELECT
  USING (auth.role() = 'authenticated');

CREATE POLICY "Owners can modify receipt translations"
  ON receipt_translations FOR ALL
  USING (
    EXISTS (SELECT 1 FROM users WHERE id::text = auth.uid()::text AND role = 'owner')
  );

